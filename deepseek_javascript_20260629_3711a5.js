let currentLang = localStorage.getItem('lang') || 'ru';

function setLanguage(lang) {
    currentLang = lang;
    document.documentElement.setAttribute('lang', lang);
    localStorage.setItem('lang', lang);

    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang] && translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });
    
    const langDisplay = document.getElementById('current-lang-display');
    if (langDisplay) {
        langDisplay.textContent = translations[lang].currentLangDisplay || lang.toUpperCase();
    }
    
    document.title = translations[lang].pageTitle || 'bebestingerman - Learn German Online';
}

// Initialize on DOM ready
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLang);

    // Mobile menu toggle
    const mobileMenuButton = document.getElementById('mobile-menu-button');
    const mobileMenu = document.getElementById('mobile-menu');
    
    if (mobileMenuButton && mobileMenu) {
        mobileMenuButton.addEventListener('click', () => {
            mobileMenu.classList.toggle('hidden');
            mobileMenuButton.setAttribute('aria-expanded', mobileMenu.classList.contains('hidden') ? 'false' : 'true');
        });

        mobileMenu.querySelectorAll('a').forEach(link => {
            link.addEventListener('click', () => {
                mobileMenu.classList.add('hidden');
                mobileMenuButton.setAttribute('aria-expanded', 'false');
            });
        });
    }

    // Language switcher dropdown
    const langSwitcherBtn = document.getElementById('lang-switcher-btn');
    const langSwitcherDropdown = document.getElementById('lang-switcher-dropdown');

    if (langSwitcherBtn && langSwitcherDropdown) {
        langSwitcherBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            langSwitcherDropdown.classList.toggle('hidden');
            langSwitcherBtn.setAttribute('aria-expanded', langSwitcherDropdown.classList.contains('hidden') ? 'false' : 'true');
        });

        langSwitcherDropdown.querySelectorAll('[data-lang-code]').forEach(button => {
            button.addEventListener('click', (event) => {
                setLanguage(event.target.getAttribute('data-lang-code'));
                langSwitcherDropdown.classList.add('hidden');
                langSwitcherBtn.setAttribute('aria-expanded', 'false');
            });
        });

        // Close dropdown on outside click
        document.addEventListener('click', (event) => {
            if (!langSwitcherBtn.contains(event.target) && !langSwitcherDropdown.contains(event.target)) {
                langSwitcherDropdown.classList.add('hidden');
                langSwitcherBtn.setAttribute('aria-expanded', 'false');
            }
        });
    }

    // Mobile menu language buttons
    if (mobileMenu) {
        mobileMenu.querySelectorAll('[data-lang-code]').forEach(button => {
            button.addEventListener('click', (event) => {
                setLanguage(event.target.getAttribute('data-lang-code'));
                mobileMenu.classList.add('hidden');
                if (mobileMenuButton) {
                    mobileMenuButton.setAttribute('aria-expanded', 'false');
                }
            });
        });
    }

    // FAQ Accordion
    document.querySelectorAll('.faq-toggle').forEach(button => {
        button.addEventListener('click', () => {
            const answer = button.nextElementSibling;
            const svg = button.querySelector('svg');
            const isExpanded = button.getAttribute('aria-expanded') === 'true';
            
            button.setAttribute('aria-expanded', !isExpanded);
            if (answer) answer.classList.toggle('hidden');
            if (svg) svg.classList.toggle('rotate-180');
        });
    });
});